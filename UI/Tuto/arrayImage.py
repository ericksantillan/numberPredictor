import numpy as np
from PIL import Image

def toMatrix(liste, columns, rows=None):
    if rows == None:
        rows = columns
    arraylist= np.asarray(liste)
    return arraylist.reshape(columns, rows)

def listToImage(liste, nb_columns, nb_rows = None):
    if nb_rows == None:
        nb_rows = nb_columns
    mat = toMatrix(liste, nb_columns, nb_rows)
    intImage = np.zeros((nb_columns, nb_rows), dtype=np.uint8)
    for i in range(0, nb_rows):
        for j in range(0,nb_columns):
            intImage[i,j] = int(255*mat[i,j])
    return intImage


